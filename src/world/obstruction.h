#pragma once
#include <cstdint>
#include <array>

// Obstruction patterns from &0100-&01a0. Each pattern is 8 bytes.
// Indexed by x-section (sub_x >> 5, i.e. 0-7 eighths of a tile horizontally).
// Each byte is a Y threshold in 0..255 tile-fraction units describing the
// surface line of the obstruction at that x-section. Point (x_frac, y_frac) is
// obstructed when threshold < y_frac (for a non-v-flipped tile), i.e. the
// point lies below the surface line.
//
// Values:
//   - All zeros (pattern 0): surface at top of tile → solid throughout
//     (everything below y=0 is solid, so entire tile is solid for y>0).
//   - All 0xff: produces "whole tile clear" for non-flipped (or all solid
//     for v-flipped). The original also triggers this via y_offset overflow.
//   - Slopes: rising/falling threshold across the 8 x-sections.
namespace Obstruction {

static constexpr std::array<std::array<uint8_t, 8>, 21> patterns = {{
    {0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}, // &00
    {0x00, 0x08, 0x10, 0x18, 0x20, 0x28, 0x30, 0x38}, // &08
    {0x00, 0x10, 0x20, 0x30, 0x40, 0x50, 0x60, 0x70}, // &10
    {0x08, 0x28, 0x48, 0x68, 0x88, 0xa8, 0xc8, 0xe8}, // &18
    {0xff, 0xff, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}, // &20
    {0x60, 0x98, 0xb8, 0xd0, 0xe0, 0xf0, 0xff, 0xff}, // &28
    {0x00, 0x00, 0x08, 0x18, 0x28, 0x40, 0x60, 0x98}, // &30
    {0x00, 0x00, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff}, // &38
    {0x38, 0x30, 0x28, 0x20, 0x18, 0x10, 0x08, 0x00}, // &40
    {0x70, 0x60, 0x50, 0x40, 0x30, 0x20, 0x10, 0x00}, // &48
    {0xe8, 0xc8, 0xa8, 0x88, 0x68, 0x48, 0x28, 0x08}, // &50
    {0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xff, 0xff}, // &58
    {0xff, 0xff, 0xf0, 0xe0, 0xd0, 0xb8, 0x98, 0x60}, // &60
    {0x98, 0x60, 0x40, 0x28, 0x18, 0x08, 0x00, 0x00}, // &68
    {0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0x00, 0x00}, // &70
    {0x40, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}, // &78
    {0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x40, 0x40}, // &80
    {0x00, 0x00, 0xff, 0xff, 0xff, 0xff, 0x00, 0x00}, // &88
    {0x40, 0x40, 0x00, 0x00, 0x00, 0x00, 0x40, 0x40}, // &90
    {0x80, 0x68, 0x48, 0x10, 0x00, 0x00, 0x00, 0x00}, // &98
    {0x00, 0x00, 0x00, 0x00, 0x10, 0x48, 0x68, 0x80}, // &a0
}};

// Port of &39dd-&39f5. Returns true iff (x_frac, y_frac) within the tile is
// obstructed by the pattern. y_offset is added to the pattern threshold (with
// overflow → 0xFF, meaning "whole tile clear" for non-flipped).
inline bool is_obstructed(int pattern_index, uint8_t x_frac, uint8_t y_frac,
                          uint8_t y_offset, bool flip_v) {
    if (pattern_index < 0 ||
        pattern_index >= static_cast<int>(patterns.size())) return false;

    int x_section = x_frac >> 5;                    // 0-7
    int t = int(patterns[pattern_index][x_section]) + int(y_offset);
    uint8_t threshold = (t > 0xFF) ? 0xFF : static_cast<uint8_t>(t);

    // Original: CMP y_frac; ROR; EOR v_flip; BPL is_obstructed.
    // obstructed iff (threshold >= y_frac) == v_flip.
    bool above_or_eq = (threshold >= y_frac);
    bool obstructed = (above_or_eq == flip_v);
    return obstructed;
}

} // namespace Obstruction
