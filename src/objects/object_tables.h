// ============================================================================
// Object Energy System (from &29ee and &29f8)
// ============================================================================

// Object types are grouped into ranges. Each range has a default energy.
// get_range_for_object_type: search object_type_ranges[] from high to low,
// first entry where object_type >= range value is the match.
static constexpr uint8_t object_type_ranges[] = {
    0x00, 0x04, 0x06, 0x0f, 0x12, 0x1c, 0x22, 0x32, 0x38, 0x4a
};

static constexpr uint8_t object_type_ranges_energy[] = {
    0x7f, 0x3f, 0x7f, 0x07, 0x3f, 0x81, 0xfd, 0x3f, 0xff, 0x7d
};

// Get initial energy for an object type (port of &2da3-&2db8)
inline uint8_t get_initial_energy(uint8_t object_type) {
    int range = 9;
    for (int x = 9; x >= 0; x--) {
        if (object_type >= object_type_ranges[x]) {
            range = x;
            break;
        }
    }
    return object_type_ranges_energy[range];
}

